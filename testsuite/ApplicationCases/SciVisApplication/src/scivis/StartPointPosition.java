package scivis;

public class StartPointPosition
{
    public int x;
    public int y;

    public StartPointPosition()
    {
        x = 0;
        y = 0;
    }

    public StartPointPosition(int x, int y)
    {
        this.x = x;
        this.y = y;
    } 
}

